using System;
using UnityEngine;
using UnityEngine.UI;
using woco.core;

namespace woco.core
{
	
	// ===================================================================================
	// CurrencyWidget
	// ===================================================================================
	public partial class CurrencyWidget : _BaseWidget
	{
	
		// - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - 
		// PROPERTIES
		// - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - 
		
		[Header("UI Elements (required)")]
		public Text amountText;
		public Image smallIcon;
		
		[Header("Template (optional)")]
		public _CurrencyTemplate template;
		
		// - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - 
		// PROTECTED FUNCTIONS
		// - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - 
		
		// -------------------------------------------------------------------------------
		// SlowUpdate
		// -------------------------------------------------------------------------------
		protected override void SlowUpdate()
		{
			if (template == null) return;
			
			smallIcon.sprite = template.smallIcon;
			amountText.text = game.currencyManager.getCurrencyAmount(template).ToString();	
		}

		// -------------------------------------------------------------------------------
		// 
		// -------------------------------------------------------------------------------

		// -------------------------------------------------------------------------------
		// 
		// -------------------------------------------------------------------------------

		// - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - 
		// EVENTS
		// - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - 
		
		// -------------------------------------------------------------------------------
		// OnInit
		// -------------------------------------------------------------------------------
		public void OnInit(_BaseInstance _instance, GameObject _window)
		{
		}

		// -------------------------------------------------------------------------------

	}
	
	// ===================================================================================
	
}
